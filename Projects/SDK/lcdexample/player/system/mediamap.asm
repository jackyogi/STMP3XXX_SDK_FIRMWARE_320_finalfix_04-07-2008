    opt     mex

    section MediaMap

;///////////////////////////////////////////////////////////////////////////////
;   Includes and external referencse
;///////////////////////////////////////////////////////////////////////////////

    nolist
    include "resource.inc"
    list

    page    132,60,0,0,0

;///////////////////////////////////////////////////////////////////////////////
;   Symbol Exports 
;///////////////////////////////////////////////////////////////////////////////

    global      MediaMapTable
                
    ; These externs are here so that the media device driver headers are
    ; referenced and are thus pulled in from the fat12 library.
    extern      SMDDHeader                                  ; DELETE

;///////////////////////////////////////////////////////////////////////////////
;   Equates
;///////////////////////////////////////////////////////////////////////////////


;///////////////////////////////////////////////////////////////////////////////
;  X memory
;///////////////////////////////////////////////////////////////////////////////


;///////////////////////////////////////////////////////////////////////////////
;  Y memory
;///////////////////////////////////////////////////////////////////////////////

    org y,"ReadFAT12_y":

    
MediaMapTable    
    ; SmartMedia device (global device number 0)
    dc      RSRC_SMDD_HIGH_CODE     ; Resource number of media device driver
    dc      0                       ; Logical device number
 if @def('MMC')    
    ; MMC device (global device number 1)
    dc      RSRC_MMCDD_CODE         ; Resource number of media device driver
    dc      0                       ; Logical device number
 else 
    ; SM device (global device number 1)
    dc      RSRC_SMDD_HIGH_CODE     ; Resource number of media device driver
    dc      1                       ; Logical device number
 endif ;@DEF(MMC)

;///////////////////////////////////////////////////////////////////////////////
;   Code
;///////////////////////////////////////////////////////////////////////////////



    endsec
 
