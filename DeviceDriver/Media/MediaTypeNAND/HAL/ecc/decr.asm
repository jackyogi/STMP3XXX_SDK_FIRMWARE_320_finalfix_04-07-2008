; Created: 07/14/00                      Last Revision: 07/14/00
;****************************************************************
; Macro to decrement a register
;****************************************************************
; Parameters: register (R0 - R7)
;****************************************************************
; Registers Altered: the one passed to it
;****************************************************************
; Stack calls required: NONE
;****************************************************************
; RETURN: Rx = Rx - 1
;****************************************************************
decr     macro xrg
        lua (xrg)-,xrg
        endm
;****************************************************************
; END Macro: decr
